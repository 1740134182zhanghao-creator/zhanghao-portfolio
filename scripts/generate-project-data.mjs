import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDirectory, "..");
const projectsRoot = path.join(projectRoot, "public", "projects");
const outputPath = path.join(projectRoot, "data", "generatedProjects.ts");

function numericValue(filename) {
  const basename = path.basename(filename, path.extname(filename));
  const match = basename.match(/(\d+)(?!.*\d)/);
  return match ? Number(match[1]) : Number.MAX_SAFE_INTEGER;
}

function sortByFilenameNumber(left, right) {
  return numericValue(left) - numericValue(right) || left.localeCompare(right, "zh-CN", { numeric: true });
}

async function readAssets(folder, extension) {
  const directory = path.join(projectsRoot, folder);
  const entries = await readdir(directory, { withFileTypes: true });

  return entries
    .filter((entry) => entry.isFile() && path.extname(entry.name).toLowerCase() === extension)
    .map((entry) => entry.name)
    .sort(sortByFilenameNumber);
}

function publicUrl(folder, filename) {
  return `/projects/${folder}/${encodeURIComponent(filename)}`;
}

function buildVideoProjects(files, folder, category, titlePrefix) {
  return files.map((filename, index) => {
    const number = numericValue(filename);
    const displayNumber = Number.isFinite(number) && number !== Number.MAX_SAFE_INTEGER ? number : index + 1;

    return {
      id: `${folder}-${String(displayNumber).padStart(2, "0")}`,
      category,
      title: `${titlePrefix} ${String(displayNumber).padStart(2, "0")}`,
      src: publicUrl(folder, filename),
    };
  });
}

const [imageFiles, aiGaibaoFiles, liveActionFiles] = await Promise.all([
  readAssets("tupian", ".png"),
  readAssets("aigaibao", ".mp4"),
  readAssets("shipai", ".mp4"),
]);

const heroImages = imageFiles.map((filename) => publicUrl("tupian", filename));
const aiGaibaoProjects = buildVideoProjects(aiGaibaoFiles, "aigaibao", "AI 改包", "AI 改包");
const liveActionProjects = buildVideoProjects(liveActionFiles, "shipai", "实拍作品", "实拍作品");

const output = `/* This file is generated by scripts/generate-project-data.mjs. */

export type GeneratedVideoProject = {
  id: string;
  category: "AI 改包" | "实拍作品";
  title: string;
  src: string;
};

export const heroImages = ${JSON.stringify(heroImages, null, 2)} as const;

export const aiGaibaoProjects: readonly GeneratedVideoProject[] = ${JSON.stringify(aiGaibaoProjects, null, 2)};

export const liveActionProjects: readonly GeneratedVideoProject[] = ${JSON.stringify(liveActionProjects, null, 2)};

export const videoProjects: readonly GeneratedVideoProject[] = [...aiGaibaoProjects, ...liveActionProjects];
`;

await writeFile(outputPath, output, "utf8");

console.log(`Generated project data: ${heroImages.length} images, ${aiGaibaoProjects.length} AI videos, ${liveActionProjects.length} live-action videos.`);
